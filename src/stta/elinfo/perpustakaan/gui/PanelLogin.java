    void status() {
        try {
            String nama = this.user.getUser().getNama();
            if (nama == null) {
                btnLogin.setText("Create User");
            } else {
                txtName.setText("");
                txtPass.setText("");
                btnLogin.setText("Login");
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    boolean cekLogin(String name, String pass) {
        try {
            if (this.user.getUser().getNama().equals(name) && this.user.getUser().getPassword().equals(pass)) {
                return true;
            } else {
                return false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(PanelLogin.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

