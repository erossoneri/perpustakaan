    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if (btnLogin.getText().equals("Create User")) {
            try {
                User us = new User();
                us.setNama(txtName.getText());
                us.setPassword(txtPass.getText());
                this.user.insert(us);
                txtName.setText("");
                txtPass.setText("");
                txtName.requestFocus();
                btnLogin.setText("Login");
                JOptionPane.showMessageDialog(this, "Data User anda Sudah tersimpan, Selamat menikmati Fasilitas Perpustakaan Mini");
            } catch (SQLException ex) {
                Logger.getLogger(PanelLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            if (cekLogin(txtName.getText(), txtPass.getText())) {
                this.setVisible(false);

            } else {
                JOptionPane.showMessageDialog(this, "Username dan password tidak sesuai");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

